# one is: one
# two is: two
# three is: three

# Because, the method re-assigns new string objects inside the method scope, but
# doesnt affect the outer scope

def mess_with_vars(one, two, three)
  one = two
  two = three
  three = one
end

one = "one"
two = "two"
three = "three"

mess_with_vars(one, two, three)

puts "one is: #{one}"
puts "two is: #{two}"
puts "three is: #{three}"

